def init_grid(inp):
	return [[c for c in line] for line in inp.split('\n') if line is not '']

grid = init_grid(inp)

def count_live_neighbours(grid, r, c):
	return sum(grid[y][x] == '#' for x in [c-1,c,c+1] for y in [r-1,r,r+1] if x < len(grid[0]) and x >= 0 and y < len(grid) and y >= 0 and (x != c or y != r))

def step1(grid):
	res = [[cell for cell in row] for row in grid]
	for row in xrange(len(grid)):
		for col in xrange(len(grid[row])):
			count = count_live_neighbours(grid, row, col)
			if grid[row][col] == '#' and count not in [2, 3]:
				res[row][col] = '.'
			elif grid[row][col] == '.' and count is 3:
				res[row][col] = '#'
	return res

for i in xrange(100):
	grid = step1(grid)

